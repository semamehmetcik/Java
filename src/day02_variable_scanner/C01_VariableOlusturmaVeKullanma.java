package day02_variable_scanner;

public class C01_VariableOlusturmaVeKullanma {
    public static void main(String[] args) {
        int sayi = 10;

        System.out.println("sayi");
        System.out.println(sayi);

        sayi = 20;

        System.out.println(sayi);

        sayi = 2 * sayi + 1;

        System.out.println(sayi);
    }
}
